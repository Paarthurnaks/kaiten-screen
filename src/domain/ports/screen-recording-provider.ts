import type { CaptureRegion } from "../value-objects/capture-region";
import type { CapturedVideo } from "../entities/captured-video";

/**
 * Порт записи видео выделенной области экрана. Симметричен ScreenCaptureProvider,
 * но с двухфазным жизненным циклом: selectRegion() показывает overlay выделения
 * области и сразу запускает запись выбранного региона (не дожидается остановки —
 * resolves, как только запись реально стартовала); stopRecording() останавливает
 * текущую запись и отдаёт результат отдельным вызовом. Без звуковой дорожки
 * (осознанное MVP-ограничение — см. ARCHITECTURE.md).
 */
export interface ScreenRecordingProvider {
  /** Overlay выделения области в режиме записи; сразу после подтверждения области
   * запись стартует (показывается плавающий индикатор с таймером и кнопкой "Стоп").
   * Возвращает выбранный регион, либо null, если пользователь отменил выделение
   * (Esc) или запись не удалось запустить. */
  selectRegion(): Promise<{ region: CaptureRegion } | null>;
  /** Останавливает текущую запись (по кнопке индикатора или по хоткею-тогглу),
   * возвращает записанное видео, либо null, если запись не была активна или
   * завершилась без данных. */
  stopRecording(): Promise<CapturedVideo | null>;
}
